class ConvertNumebrs:
    def number_to_float(self, number: int):
        return float(number)

    def float_to_number(self, float: float):
        return int(float)

    def number_to_complex(self, number: int):
        return complex(number)

    def number_to_string(self, number: int):
        return str(number)

    def string_to_number(self, string: str):
        return int(string)
