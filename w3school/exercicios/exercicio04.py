class ClasseTypos:
    def typeNumber(self):
        x = 5
        return type(x)

    def typeString(self):
        x = "Hello World"
        return type(x)

    def typeFloat(self):
        x = 20.5
        return type(x)

    def typeList(self):
        x = ["apple", "banana", "cherry"]
        return type(x)

    def typeTuple(self):
        x = ("apple", "banana", "cherry")
        return type(x)

    def typeDict(self):
        x = {"name": "John", "age": 36}
        return type(x)

    def typeBool(self):
        x = True
        return type(x)
